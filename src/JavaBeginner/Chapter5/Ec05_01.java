package JavaBeginner.Chapter5;

public class Ec05_01 {
    public static void main(String[] args) {
        int a = 99;
        //true일때만 출력된다.
        if(a<100) System.out.printf("\n100보다 작습니다.\n");//true

        if(a==100) System.out.printf("\n100과 같습니다.\n");//false

        if(a>100) System.out.printf("\n100보다 큽니다.\n");//false
    }//main e
}
